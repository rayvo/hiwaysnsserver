package kr.co.ex.hiwaysns;

public class TroasisCCTV {
	private int id;
	private String cctvId;
	private int roadNo;
	private String cctvLoc;
	private int cctvLng;
	private int cctvLat;
	private String cctvURL;
	private String cctvAddress;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCctvId() {
		return cctvId;
	}
	public void setCctvId(String cctvId) {
		this.cctvId = cctvId;
	}
	public int getRoadNo() {
		return roadNo;
	}
	public void setRoadNo(int roadNo) {
		this.roadNo = roadNo;
	}
	public String getCctvLoc() {
		return cctvLoc;
	}
	public void setCctvLoc(String cctvLoc) {
		this.cctvLoc = cctvLoc;
	}
	public int getCctvLng() {
		return cctvLng;
	}
	public void setCctvLng(int cctvLng) {
		this.cctvLng = cctvLng;
	}
	public int getCctvLat() {
		return cctvLat;
	}
	public void setCctvLat(int cctvLat) {
		this.cctvLat = cctvLat;
	}
	public String getCctvURL() {
		return cctvURL;
	}
	public void setCctvURL(String cctvURL) {
		this.cctvURL = cctvURL;
	}
	public String getCctvAddress() {
		return cctvAddress;
	}
	public void setCctvAddress(String cctvAddress) {
		this.cctvAddress = cctvAddress;
	}
	
	
}
